export const getTranslatedEntry = (entries: any[], lang: string) => {
  if (!entries || !Array.isArray(entries)) return null;
  let entry = entries.find(e => e.language.name === lang);
  if (!entry && lang === 'pt-BR') {
    entry = entries.find(e => e.language.name === 'pt');
    // Removed Spanish fallback to prevent confusion. Will fallback to English below.
  }
  if (!entry && lang === 'es') {
    entry = entries.find(e => e.language.name === 'es');
  }
  if (!entry) {
    entry = entries.find(e => e.language.name === 'en');
  }
  return entry;
};
